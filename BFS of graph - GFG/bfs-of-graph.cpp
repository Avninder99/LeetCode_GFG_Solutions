class Solution {
  public:
    // Function to return Breadth First Traversal of given graph.
    vector<int> bfsOfGraph(int V, vector<int> adj[]) {
        // Code here
        vector<int>rec(V, 0), res;
        queue<int>q1;
        q1.push(0);
        rec[0] = 1;
        while(!q1.empty()){
            int t = q1.front();
            q1.pop();
            res.push_back(t);
            for(auto itr: adj[t]){
                if(!rec[itr]){
                    q1.push(itr);
                    rec[itr] = 1;
                }
            }
        }
        return res;
        
    }
};

