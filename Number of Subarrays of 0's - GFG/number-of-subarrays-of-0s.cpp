long long int no_of_subarrays(int n, vector<int> &arr) {
    // Write your code here.
    long long int res = 0;
    int len = 0;
    arr.push_back(1);
    for(auto itr: arr){
        if(itr == 0){
            len++;
        }else{
            while(len){
                res += len--;
            }
        }
    }
    return res;
}

